export type VSCodeTheme = typeof vscodeDarkTheme | typeof vscodeLightTheme

/**
 * 初始化 VSCode 主题变量
 * @param theme 主题类型，默认为 'dark'
 */
export function initVSCodeTheme(theme: 'dark' | 'light' = 'dark'): void {
	const themeVars = theme === 'dark' ? vscodeDarkTheme : vscodeLightTheme
	const root = document.documentElement

	// 应用所有 CSS 变量到根元素
	Object.entries(themeVars).forEach(([property, value]) => {
		root.style.setProperty(property, value)
	})

	// 设置 body 的背景色和前景色
	document.body.style.backgroundColor = themeVars["--vscode-editor-background"]
	document.body.style.color = themeVars["--vscode-editor-foreground"]
	document.body.style.fontFamily = themeVars["--vscode-font-family"]
	document.body.style.fontSize = themeVars["--vscode-font-size"]
}

/**
 * 检测系统主题偏好
 */
export function detectSystemTheme(): 'dark' | 'light' {
	if (typeof window !== 'undefined' && window.matchMedia) {
		return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light'
	}
	return 'dark'
}
